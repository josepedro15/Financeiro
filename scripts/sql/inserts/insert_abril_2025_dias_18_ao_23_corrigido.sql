-- 1. Verificar inserção por dia
SELECT 
    '📊 VERIFICAÇÃO POR DIA:' as status,
    transaction_date,
    COUNT(*) as total_transacoes,
    SUM(amount) as valor_total,
    ROUND(AVG(amount), 2) as valor_medio
FROM public.transactions_2025_04
WHERE transaction_date IN ('2025-04-18', '2025-04-19', '2025-04-21', '2025-04-22', '2025-04-23')
GROUP BY transaction_date
ORDER BY transaction_date;

-- 2. Verificar por conta
SELECT 
    '💳 VERIFICAÇÃO POR CONTA:' as status,
    account_name,
    COUNT(*) as total_transacoes,
    SUM(amount) as valor_total
FROM public.transactions_2025_04
WHERE transaction_date IN ('2025-04-18', '2025-04-19', '2025-04-21', '2025-04-22', '2025-04-23')
GROUP BY account_name
ORDER BY account_name;

-- 3. Resumo total
SELECT 
    '🎯 RESUMO TOTAL DOS DIAS 18-23:' as status,
    COUNT(*) as total_transacoes,
    SUM(amount) as valor_total,
    MIN(transaction_date) as primeira_data,
    MAX(transaction_date) as ultima_data
FROM public.transactions_2025_04
WHERE transaction_date IN ('2025-04-18', '2025-04-19', '2025-04-21', '2025-04-22', '2025-04-23');

-- 4. Estado completo de abril
SELECT 
    '📅 ESTADO COMPLETO DE ABRIL:' as status,
    EXTRACT(DAY FROM transaction_date) as dia,
    COUNT(*) as total_transacoes,
    SUM(amount) as valor_total
FROM public.transactions_2025_04
WHERE EXTRACT(YEAR FROM transaction_date) = 2025 
  AND EXTRACT(MONTH FROM transaction_date) = 4
GROUP BY EXTRACT(DAY FROM transaction_date)
ORDER BY dia;
